package me.gurpreet.collabratevid.models;

import io.realm.RealmObject;

public class DrawPoint extends RealmObject {
    private double x;
    private double y;

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
